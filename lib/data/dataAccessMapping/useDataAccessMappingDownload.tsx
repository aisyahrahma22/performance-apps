import axios from '../../axios';
import fileDownload from 'js-file-download';
import { AxiosResponse } from 'axios';
import getFilenameFromHeader from '../../util/getFilenameFromHeader';
import { toast } from 'react-toastify';

const API_DATA_ACCESS_MAPPING_DOWNLOAD = '/api/DataAccessMapping/download';

export const getDataAccessMappingDownload = () => {
  return axios
    .get(API_DATA_ACCESS_MAPPING_DOWNLOAD, {
      responseType: 'blob',
    })
    .then((resp: AxiosResponse) => {
      fileDownload(resp.data, getFilenameFromHeader(resp));
    })
    .catch((e) => {
      toast.error(
        e.response.data.message ||
          'Error when downloading Data Access Mapping Format file',
      );
    });
};
