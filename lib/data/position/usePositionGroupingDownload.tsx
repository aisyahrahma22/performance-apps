import axios from '../../axios';
import fileDownload from 'js-file-download';
import { AxiosResponse } from 'axios';
import getFilenameFromHeader from '../../util/getFilenameFromHeader';
import { toast } from 'react-toastify';

const API_POSITION_GROUPING_DOWNLOAD = '/api/position/grouping/download';

export const getPositionGroupingDownload = () => {
  return axios
    .get(API_POSITION_GROUPING_DOWNLOAD, {
      responseType: 'blob',
    })
    .then((resp: AxiosResponse) => {
      fileDownload(resp.data, getFilenameFromHeader(resp));
    })
    .catch((e) => {
      toast.error(
        e.response.data.message ||
          'Error when downloading Position Grouping Format file',
      );
    });
};
